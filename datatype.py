x = "Hello World"
print(x)
print(type(x)) 

x = 20
print(x)
print(type(x)) 

x = 20.5
print(x)
print(type(x)) 

x = 1j
print(x)
print(type(x)) 


x = ["apple", "banana", "cherry"]
print(x)
print(type(x)) 

x = ("apple", "banana", "cherry")
print(x)
print(type(x)) 

x = range(6)
print(x)
print(type(x)) 

x = {"name" : "John", "age" : 36}
print(x)
print(type(x)) 


x = {"apple", "banana", "cherry"}
print(x)
print(type(x)) 


x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x)) 


x = True
print(x)
print(type(x)) 

x = b"Hello"
print(x)
print(type(x)) 

x = bytearray(5)
print(x)
print(type(x)) 


x = memoryview(bytes(5))
print(x)
print(type(x)) 


x = None
print(x)
print(type(x)) 

